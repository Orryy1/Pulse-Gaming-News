"use strict";

const { execFileSync } = require("node:child_process");
const path = require("node:path");

const {
  buildWindowsSchedulerHygiene,
} = require("./local-restart-readiness");

function quotePowerShellSingle(value) {
  return `'${String(value || "").replace(/'/g, "''")}'`;
}

function safeTaskBackupName(taskName) {
  return `${String(taskName || "unnamed_task")
    .replace(/[^A-Za-z0-9_.-]+/g, "_")
    .replace(/^_+|_+$/g, "") || "unnamed_task"}.xml`;
}

function executableBasename(execute) {
  const cleaned = String(execute || "")
    .trim()
    .replace(/^["']|["']$/g, "");
  const parts = cleaned.split(/[\\/]/).filter(Boolean);
  return (parts[parts.length - 1] || cleaned).toLowerCase();
}

function windowlessExecute(execute) {
  const value = String(execute || "").trim();
  const base = executableBasename(value);
  if (base === "python") return "pythonw.exe";
  if (base === "python.exe") return value.replace(/python\.exe$/i, "pythonw.exe");
  return value;
}

function enrichWorkOrder(workOrder, { outDir = path.join(process.cwd(), "test", "output") } = {}) {
  const backupPath = path.join(
    outDir,
    "scheduler_task_backups",
    safeTaskBackupName(workOrder.task_name),
  );
  return {
    ...workOrder,
    backup_path: backupPath,
    apply_command:
      "npm run ops:windows-scheduler-repair -- --apply --operator-confirmed --json",
  };
}

function buildElevatedRepairPacket(
  plan,
  { cwd = process.cwd(), outDir = path.join(cwd, "test", "output") } = {},
) {
  const workOrders = plan?.work_orders || [];
  const scriptPath = path.join(
    outDir,
    "windows_scheduler_hidden_launcher_repair_elevated.ps1",
  );
  const validationCommand =
    workOrders.find((workOrder) => workOrder.post_repair_validation_command)
      ?.post_repair_validation_command || "npm run ops:local-restart-readiness -- --json";
  const repairBlocks = workOrders.flatMap((workOrder) => [
    "",
    `Write-Host ${quotePowerShellSingle(`Repairing ${workOrder.task_path || "\\"}${workOrder.task_name}`)}`,
    buildRepairPowerShell(workOrder),
  ]);
  const script = [
    "# Windows Scheduler Hidden Launcher Repair",
    "# Generated by npm run ops:windows-scheduler-repair.",
    "# Safety: No tokens, OAuth, DB rows or platform posts are changed.",
    "# Scope: backs up matching Task Scheduler tasks, then changes python to pythonw.exe.",
    "",
    "$ErrorActionPreference = 'Stop'",
    "$identity = [Security.Principal.WindowsIdentity]::GetCurrent()",
    "$principal = New-Object Security.Principal.WindowsPrincipal($identity)",
    "if (-not $principal.IsInRole([Security.Principal.WindowsBuiltInRole]::Administrator)) {",
    "  throw 'Run this script from an Administrator PowerShell session.'",
    "}",
    `Set-Location ${quotePowerShellSingle(cwd)}`,
    `Write-Host ${quotePowerShellSingle(`Repair work orders: ${workOrders.length}`)}`,
    ...repairBlocks,
    "",
    `Write-Host ${quotePowerShellSingle("Repair packet finished. Run validation next:")}`,
    `Write-Host ${quotePowerShellSingle(validationCommand)}`,
    "",
  ].join("\r\n");

  return {
    script_path: scriptPath,
    run_command: `powershell.exe -NoProfile -ExecutionPolicy Bypass -File "${scriptPath}"`,
    work_order_count: workOrders.length,
    validation_command: validationCommand,
    safety:
      "Run from Administrator PowerShell only; changes Task Scheduler actions from python to pythonw.exe after exporting backups.",
    script,
  };
}

function buildWindowsSchedulerHiddenLauncherRepairPlan({
  cwd = process.cwd(),
  platform = process.platform,
  scheduledTasks,
  execFileSyncImpl = execFileSync,
  generatedAt = new Date().toISOString(),
  outDir = path.join(cwd, "test", "output"),
} = {}) {
  const hygiene = buildWindowsSchedulerHygiene({
    cwd,
    platform,
    scheduledTasks,
    execFileSyncImpl,
  });
  const workOrders = (hygiene.repair_work_orders || []).map((workOrder) =>
    enrichWorkOrder(workOrder, { outDir }),
  );
  const blockedReason =
    platform !== "win32"
      ? "not_windows"
      : hygiene.query_status === "failed"
        ? "scheduler_query_failed"
        : workOrders.length
          ? "awaiting_operator_apply_in_elevated_powershell"
          : null;

  return {
    schema_version: 1,
    generated_at: generatedAt,
    mode: "dry_run_no_os_mutation",
    verdict: workOrders.length
      ? "red"
      : hygiene.query_status === "failed"
        ? "amber"
        : "green",
    safety:
      "Dry-run by default; does not mutate Task Scheduler, tokens, OAuth, DB rows or platform posting state.",
    summary: {
      inspected: hygiene.inspected === true,
      query_status: hygiene.query_status,
      relevant_task_count: hygiene.relevant_task_count || 0,
      visible_console_risk_count: hygiene.visible_console_risk_count || 0,
      repairable_count: workOrders.length,
      risk_task_names: hygiene.risk_task_names || [],
    },
    scheduler_hygiene: hygiene,
    work_orders: workOrders,
    blocked_apply_reason: blockedReason,
  };
}

function buildRepairPowerShell(workOrder) {
  const nextExecute = windowlessExecute(workOrder.current_execute || workOrder.currentExecute);
  const actionParts = [
    "-Execute",
    quotePowerShellSingle(nextExecute),
  ];
  if (workOrder.current_arguments) {
    actionParts.push("-Argument", quotePowerShellSingle(workOrder.current_arguments));
  }
  if (workOrder.current_working_directory) {
    actionParts.push(
      "-WorkingDirectory",
      quotePowerShellSingle(workOrder.current_working_directory),
    );
  }

  return [
    "$ErrorActionPreference = 'Stop'",
    `$backupPath = ${quotePowerShellSingle(workOrder.backup_path)}`,
    "$backupDir = Split-Path -Parent $backupPath",
    "New-Item -ItemType Directory -Force -Path $backupDir | Out-Null",
    `Export-ScheduledTask -TaskName ${quotePowerShellSingle(workOrder.task_name)} -TaskPath ${quotePowerShellSingle(workOrder.task_path || "\\")} | Out-File -FilePath $backupPath -Encoding utf8`,
    `$action = New-ScheduledTaskAction ${actionParts.join(" ")}`,
    `Set-ScheduledTask -TaskName ${quotePowerShellSingle(workOrder.task_name)} -TaskPath ${quotePowerShellSingle(workOrder.task_path || "\\")} -Action $action -ErrorAction Stop | Out-Null`,
  ].join("\n");
}

function classifyApplyError(error) {
  const text = [
    error?.message,
    error?.stderr && Buffer.isBuffer(error.stderr) ? error.stderr.toString("utf8") : error?.stderr,
    error?.stdout && Buffer.isBuffer(error.stdout) ? error.stdout.toString("utf8") : error?.stdout,
  ]
    .filter(Boolean)
    .join("\n");
  if (/access is denied|0x80070005|permissiondenied/i.test(text)) {
    return {
      status: "blocked_requires_elevated_powershell",
      requires_elevated_shell: true,
      operator_action:
        "Run the same apply command from an elevated PowerShell session, then rerun scheduler readiness.",
    };
  }
  return {
    status: "failed",
    requires_elevated_shell: false,
    operator_action: "Inspect the Task Scheduler error and rerun the dry-run repair report.",
  };
}

function assertApplyAllowed({ operatorConfirmed }) {
  if (operatorConfirmed !== true) {
    throw new Error("windows_scheduler_repair_apply_requires_operator_confirmed_flag");
  }
}

function applyWindowsSchedulerHiddenLauncherRepair({
  plan,
  operatorConfirmed = false,
  execFileSyncImpl = execFileSync,
} = {}) {
  assertApplyAllowed({ operatorConfirmed });
  const workOrders = plan?.work_orders || [];
  const results = [];
  for (const workOrder of workOrders) {
    const command = buildRepairPowerShell(workOrder);
    try {
      execFileSyncImpl(
        "powershell.exe",
        [
          "-NoProfile",
          "-NonInteractive",
          "-ExecutionPolicy",
          "Bypass",
          "-Command",
          command,
        ],
        {
          encoding: "utf8",
          timeout: 30000,
          windowsHide: true,
          stdio: ["ignore", "pipe", "pipe"],
        },
      );
      results.push({
        task_name: workOrder.task_name,
        task_path: workOrder.task_path,
        status: "applied",
        backup_path: workOrder.backup_path,
        next_execute: windowlessExecute(workOrder.current_execute),
        validation_command: workOrder.post_repair_validation_command,
      });
    } catch (error) {
      const classified = classifyApplyError(error);
      results.push({
        task_name: workOrder.task_name,
        task_path: workOrder.task_path,
        status: classified.status,
        backup_path: workOrder.backup_path,
        requires_elevated_shell: classified.requires_elevated_shell,
        operator_action: classified.operator_action,
        validation_command: workOrder.post_repair_validation_command,
      });
    }
  }

  const applied = results.filter((result) => result.status === "applied").length;
  const blocked = results.filter((result) => result.status !== "applied").length;
  return {
    schema_version: 1,
    generated_at: new Date().toISOString(),
    mode: "apply_os_task_scheduler_mutation",
    safety:
      "Task Scheduler action repair only; no tokens, OAuth, DB rows, platform posting or publish gates changed.",
    counts: {
      planned: workOrders.length,
      applied,
      blocked,
    },
    results,
  };
}

function formatWindowsSchedulerHiddenLauncherRepairMarkdown(plan) {
  const lines = [];
  lines.push("# Windows Scheduler Hidden Launcher Repair");
  lines.push("");
  lines.push(`Generated: ${plan.generated_at}`);
  lines.push(`Verdict: ${String(plan.verdict || "unknown").toUpperCase()}`);
  lines.push(`Mode: ${plan.mode === "dry_run_no_os_mutation" ? "Dry-run" : plan.mode}`);
  lines.push(`Safety: ${plan.safety}`);
  lines.push("");
  lines.push("## Summary");
  lines.push(`- Query status: ${plan.summary?.query_status || "unknown"}`);
  lines.push(`- Relevant tasks: ${plan.summary?.relevant_task_count || 0}`);
  lines.push(`- Visible-console risks: ${plan.summary?.visible_console_risk_count || 0}`);
  lines.push(`- Repairable tasks: ${plan.summary?.repairable_count || 0}`);
  if (plan.summary?.risk_task_names?.length) {
    lines.push(`- Risk tasks: ${plan.summary.risk_task_names.join(", ")}`);
  }
  if (plan.work_orders?.length) {
    lines.push("");
    lines.push("## Repair Work Orders");
    for (const workOrder of plan.work_orders) {
      lines.push(`- Work order: ${workOrder.work_order_id}`);
      lines.push(`  - Task: ${workOrder.task_path}${workOrder.task_name}`);
      lines.push(`  - Current execute: ${workOrder.current_execute || "(blank)"}`);
      lines.push(`  - Backup path: ${workOrder.backup_path}`);
      lines.push(`  - Requires elevated PowerShell: ${String(workOrder.requires_elevated_shell)}`);
      lines.push(`  - Apply: \`${workOrder.apply_command}\``);
      lines.push(`  - Validate: \`${workOrder.post_repair_validation_command}\``);
    }
  }
  if (plan.elevated_repair_packet) {
    lines.push("");
    lines.push("## Elevated Repair Packet");
    lines.push(`- Script: ${plan.elevated_repair_packet.script_path}`);
    lines.push(`- Run: \`${plan.elevated_repair_packet.run_command}\``);
    lines.push(`- Validate: \`${plan.elevated_repair_packet.validation_command}\``);
    lines.push(`- Safety: ${plan.elevated_repair_packet.safety}`);
  }
  if (plan.apply_result) {
    lines.push("");
    lines.push("## Apply Result");
    lines.push(`- Applied: ${plan.apply_result.counts?.applied || 0}`);
    lines.push(`- Blocked: ${plan.apply_result.counts?.blocked || 0}`);
    for (const result of plan.apply_result.results || []) {
      lines.push(`- ${result.task_name}: ${result.status}`);
      if (result.operator_action) lines.push(`  - Operator action: ${result.operator_action}`);
    }
  }
  if (plan.blocked_apply_reason) {
    lines.push("");
    lines.push(`Blocked apply reason: ${plan.blocked_apply_reason}`);
  }
  return lines.join("\n");
}

