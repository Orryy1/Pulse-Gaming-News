"use strict";

const http = require("node:http");
const https = require("node:https");
const path = require("node:path");

function resolveLocalAuthBaseUrl(env = process.env) {
  const explicit = String(env.TIKTOK_AUTH_SERVER_URL || "").trim();
  if (explicit) return explicit.replace(/\/+$/, "");
  const port = String(env.PORT || "3001").trim() || "3001";
  return `http://127.0.0.1:${port}`;
}

function requestLocalAuthOnce(url, { headers = {}, timeoutMs = 15_000 } = {}) {
  return new Promise((resolve, reject) => {
    let parsed;
    try {
      parsed = new URL(url);
    } catch {
      reject(new Error("invalid local auth URL"));
      return;
    }

    const client = parsed.protocol === "https:" ? https : http;
    const req = client.request(
      parsed,
      {
        method: "GET",
        headers,
      },
      (res) => {
        res.resume();
        res.on("end", () => {
          resolve({
            status: res.statusCode || 0,
            headers: res.headers || {},
          });
        });
      },
    );
    req.setTimeout(timeoutMs, () => {
      req.destroy(new Error("local TikTok auth initiator timed out"));
    });
    req.on("error", reject);
    req.end();
  });
}

async function fetchTikTokAuthRedirect({
  env = process.env,
  requestOnce = requestLocalAuthOnce,
  baseUrl = resolveLocalAuthBaseUrl(env),
} = {}) {
  const apiToken = String(env.API_TOKEN || "").trim();
  if (!apiToken) {
    throw new Error("API_TOKEN is required to generate the protected TikTok auth link");
  }

  const url = `${String(baseUrl || "").replace(/\/+$/, "")}/auth/tiktok`;
  const response = await requestOnce(url, {
    headers: {
      Authorization: `Bearer ${apiToken}`,
    },
  });

  if (response.status === 401 || response.status === 403) {
    throw new Error("server rejected API_TOKEN while generating TikTok auth link");
  }

  const location = response.headers?.location;
  if (!location) {
    throw new Error(`TikTok auth initiator did not return a redirect location (status ${response.status})`);
  }

  return {
    authUrl: location,
    serverUrl: url,
    expiresInMinutes: 10,
  };
}

