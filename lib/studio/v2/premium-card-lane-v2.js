/**
 * lib/studio/v2/premium-card-lane-v2.js — premium card routing v2.
 *
 * Extends the v1 lane with two more HyperFrames cards:
 *   - card.quote     -> experiments/hf-quote/quote-card.mp4
 *   - card.takeaway  -> experiments/hf-takeaway/takeaway-card.mp4
 *
 * V1 already routes:
 *   - card.source   -> hf-source
 *   - card.stat     -> hf-context (replaces a duplicate source card)
 *
 * In v2, four card types ride the HyperFrames lane.
 *
 * If a HyperFrames card is missing, the scene falls back to its
 * ffmpeg renderer. Verdict goes "pass" if all attached HF cards
 * are present, "partial" otherwise.
 */

"use strict";

const path = require("node:path");
const fs = require("fs-extra");
const { SCENE_TYPES } = require("../../scene-composer");
const {
  applyPremiumCardLane: applyPremiumCardLaneV1,
} = require("../premium-card-lane");

function firstExisting(paths) {
  return paths.find((p) => p && fs.existsSync(p)) || null;
}

function resolveCardAssetsV2(root, storyId) {
  // Story-specific quote card takes precedence over the generic
  // template — generated by tools/studio-v2-build-quote-card.js when
  // a per-story quote needs swapping in.
  const storyQuotePath = storyId
    ? path.join(root, "test", "output", `hf_quote_card_${storyId}.mp4`)
    : null;
  return {
    sourceCard: firstExisting([
      path.join(root, "test", "output", "hf_source_card_v1.mp4"),
      path.join(root, "experiments", "hf-source", "source-card.mp4"),
    ]),
    contextCard: firstExisting([
      path.join(root, "test", "output", "hf_context_card_v1.mp4"),
      path.join(root, "experiments", "hf-context", "context-card.mp4"),
    ]),
    quoteCard: firstExisting([
      storyQuotePath,
      path.join(root, "test", "output", "hf_quote_card_v1.mp4"),
      path.join(root, "experiments", "hf-quote", "quote-card.mp4"),
    ]),
    quoteCardStorySpecific: storyQuotePath && fs.existsSync(storyQuotePath),
    takeawayCard: firstExisting([
      path.join(root, "test", "output", "hf_takeaway_card_v1.mp4"),
      path.join(root, "experiments", "hf-takeaway", "takeaway-card.mp4"),
    ]),
  };
}

function applyPremiumCardLaneV2({ scenes, story, root }) {
  // Run the v1 pass first — it handles source + context cards.
  const v1Result = applyPremiumCardLaneV1({ scenes, story, root });
  const out = v1Result.scenes.map((scene) => ({ ...scene }));
  const assets = resolveCardAssetsV2(root, story?.id);
  const decisions = [...(v1Result.premiumLane.decisions || [])];

  let quoteAttached = null;
  let takeawayAttached = null;

  for (const scene of out) {
    if (scene.type === SCENE_TYPES.CARD_QUOTE && !scene.prerenderedMp4) {
      if (assets.quoteCard) {
        scene.prerenderedMp4 = assets.quoteCard;
        scene.premiumLane = "hyperframes";
        quoteAttached = assets.quoteCardStorySpecific
          ? "hyperframes-story-specific"
          : "hyperframes";
        decisions.push({
          scene: scene.label,
          type: scene.type,
          renderer: "hyperframes",
          reason: assets.quoteCardStorySpecific
            ? "story-specific quote card found — using per-story render"
            : "quote card is typographic and reads better as HTML/SVG",
        });
      } else {
        quoteAttached = "ffmpeg-fallback";
        decisions.push({
          scene: scene.label,
          type: scene.type,
          renderer: "ffmpeg-fallback",
          reason: "HyperFrames quote card not rendered yet",
        });
      }
    }

    if (scene.type === SCENE_TYPES.CARD_TAKEAWAY && !scene.prerenderedMp4) {
      if (assets.takeawayCard) {
        scene.prerenderedMp4 = assets.takeawayCard;
        scene.premiumLane = "hyperframes";
        takeawayAttached = "hyperframes";
        decisions.push({
          scene: scene.label,
          type: scene.type,
          renderer: "hyperframes",
          reason: "takeaway card benefits from the kinetic word reveal",
        });
      } else {
        takeawayAttached = "ffmpeg-fallback";
        decisions.push({
          scene: scene.label,
          type: scene.type,
          renderer: "ffmpeg-fallback",
          reason: "HyperFrames takeaway card not rendered yet",
        });
      }
    }
  }

  const hfCount = out.filter((s) => s.premiumLane === "hyperframes").length;
  const verdict = hfCount >= 3 ? "pass" : hfCount >= 2 ? "partial" : "thin";

  return {
    scenes: out,
    premiumLane: {
      ...v1Result.premiumLane,
      quoteCard: quoteAttached,
      takeawayCard: takeawayAttached,
      hyperframesCardCount: hfCount,
      verdict,
      decisions,
      assetsV2: assets,
    },
  };
}

module.exports = {
  applyPremiumCardLaneV2,
  resolveCardAssetsV2,
};
